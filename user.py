class user:
    def getusername(self):
        return self.name
    def setusername(self, name):
        self.name = name
    def getscore(self):
        return self.score
    def setscore(self, score):
        self.score = score
    def getstate(self):
        return self.state
    def setstate(self, state):
        self.state = state